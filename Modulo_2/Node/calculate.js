module.exports = {suma,resta,multiplicar,dividir}


function suma(a,b) {
    suma = a + b
    return suma 
};

function resta(a,b) {
    resta = a - b
    return resta 
};

function multiplicar(a,b) {
    multiplicar = a * b
    return multiplicar 
};

function dividir(a,b) {
    dividir = a / b
    return dividir 
};

